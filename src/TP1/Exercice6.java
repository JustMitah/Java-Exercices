    private void OpenActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OpenActionPerformed
                JFileChooser chooser = new JFileChooser();      
                FileNameExtensionFilter filter_txt = new FileNameExtensionFilter("Text Documents", "txt");
                FileNameExtensionFilter filter_py = new FileNameExtensionFilter("Python Files", "py");
                chooser.addChoosableFileFilter(filter_txt);
                chooser.addChoosableFileFilter(filter_py);
                int retval = chooser.showDialog(chooser, "Open");
                if (retval == JFileChooser.APPROVE_OPTION) 
                {
                    File choosen_file = chooser.getSelectedFile();
                    try {
                        DisplayArea.setText(ReadFile(choosen_file));
                    } catch (IOException ex) {
                        Logger.getLogger(Exercice6.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
    }//GEN-LAST:event_OpenActionPerformed

