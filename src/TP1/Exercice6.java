        private String ReadFile(File file) throws IOException{
            FileReader f = null;
            try{
                f = new FileReader(file);
            } catch(IOException e){
                System.out.println("Erreur d'ouverture du fichier");
                System.out.println(e.getMessage());
                System.exit(0);
            }
        int c;
        while ((c=f.read()) !=-1){
           file_content += (char)c;
        }
        f.close();
        return file_content;
        }
